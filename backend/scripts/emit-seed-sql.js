/**
 * Emits an idempotent SQL insert for the 2025 seed TaxConfig so the backend
 * table can be populated before the AI job runs. Transpiles the typed seed
 * (its only import is type-only and is erased by preset-typescript) and writes
 * backend/supabase/migrations/0002_seed_2025.sql.
 *
 * Usage: node backend/scripts/emit-seed-sql.js
 */
const fs = require('fs');
const path = require('path');
const babel = require('@babel/core');

const root = path.resolve(__dirname, '..', '..');
const seedPath = path.join(root, 'src', 'tax-config', 'seed-2025.ts');
const outPath = path.join(root, 'backend', 'supabase', 'migrations', '0002_seed_2025.sql');

const src = fs.readFileSync(seedPath, 'utf8');
const { code } = babel.transformSync(src, {
  filename: 'seed-2025.ts',
  presets: [['@babel/preset-env', { targets: { node: 'current' } }], '@babel/preset-typescript'],
});

const moduleObj = { exports: {} };
new Function('module', 'exports', 'require', code)(moduleObj, moduleObj.exports, require);
const seed = moduleObj.exports.SEED_TAX_CONFIG_2025;

const json = JSON.stringify(seed).replace(/'/g, "''");
const sources = seed.source_urls.map((u) => `'${u.replace(/'/g, "''")}'`).join(', ');

const sql = `-- Generated by backend/scripts/emit-seed-sql.js — do not edit by hand.
insert into public.tax_configs (tax_year, config, last_updated, source_urls)
values (${seed.tax_year}, '${json}'::jsonb, '${seed.last_updated}', array[${sources}]::text[])
on conflict (tax_year) do update
  set config = excluded.config,
      last_updated = excluded.last_updated,
      source_urls = excluded.source_urls;
`;

fs.writeFileSync(outPath, sql);
console.log(`Wrote ${outPath} (${json.length} bytes of config)`);
